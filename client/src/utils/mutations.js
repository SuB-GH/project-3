import { gql } from '@apollo/client';


export const ADD_BOOK = gql`
  mutation addBook($username: String!, $email: String!, $password: String!) {
    addUser(username: $username, email: $email, password: $password) {
      token
      user {
        _id
        username
      }
    }
  }
`;




